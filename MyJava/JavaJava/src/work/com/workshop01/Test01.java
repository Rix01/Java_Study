package work.com.workshop01;

public class Test01 {

	public static void main(String[] args) {
		int a = 100;
		double b = 3.14;
		char c = 'A';
		boolean d = true;

		System.out.printf("정수형 변수의 값은 %d이며, 자료형은 int입니다.\n", a);
		System.out.printf("정수형 변수의 값은 %.2f이며, 자료형은 double입니다.\n", b);
		System.out.printf("정수형 변수의 값은 %c이며, 자료형은 char입니다.\n", c);
		System.out.printf("정수형 변수의 값은 %b이며, 자료형은 boolean입니다.\n", d);
	}
}
